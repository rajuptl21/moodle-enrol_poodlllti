class enrol_poodlllti_generator extends enrol_lti_generator {

    /**
     * Create a test course category
     * @param array|stdClass $record
     * @param array $options
     * @return core_course_category course category record
     */
    public function create_empty_category($record = null, ?array $options = null) {
        global $DB;
        $i = $DB->count_records('course_categories');
        $i++;

        $record = (array)$record;

        if (!isset($record['name'])) {
            $record['name'] = autogeneratedata::TENANT_NAME . " $i";
        }

        if (!isset($record['description'])) {
            $record['description'] = autogeneratedata::TENANT_NAME;
        }

        if (!isset($record['idnumber'])) {
            $record['idnumber'] = '';
        }

        return $this->datagenerator->create_category($record, $options);
    }

    public function create_empty_course($record = null, ?array $options = null) {
        global $DB, $CFG;
        require_once("$CFG->dirroot/course/lib.php");

        $record = (array)$record;

        if (empty($record['category'])) {
            $record['category'] = $DB->get_field_select('course_categories', "MIN(id)", "parent=0");
        }

        $i = $DB->count_records('course', ['category' => $record['category']]);
        $i++;

        if (!isset($record['fullname'])) {
            $record['fullname'] = autogeneratedata::TENANT_COURSE_NAME . " $i";
        }

        if (!isset($record['shortname'])) {
            $record['shortname'] = $record['category'] . ':tc_' . $i;
        }

        if (!isset($record['idnumber'])) {
            $record['idnumber'] = '';
        }

        if (!isset($record['summary'])) {
            $record['summary'] = autogeneratedata::TENANT_COURSE_NAME;
        }

        return $this->datagenerator->create_course($record, $options);
    }
}
